def numero_a_giorno(numero):
    giorni_settimana = {
        1: "lunedì",
        2: "martedì",
        3: "mercoledì",
        4: "giovedì",
        5: "venerdì",
        6: "sabato",
        7: "domenica"
    }
    return giorni_settimana[numero]

